// Problem 4 Solution

// Declare an array of numbers
let numbersArray = [1, 2, 3];

// Iterate through the array and log each element multiplied by 2
for (let i = 0; i < numbersArray.length; i++) {
    console.log(numbersArray[i] * 2);
}

